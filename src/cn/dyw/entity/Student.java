package cn.dyw.entity;

public class Student {
	
	private String name;
	
	private int age;
	
	private int sex;
	
	private String jieSao;
	
	private String date;
	
	private int fuDaoYuan;

	private String interesters;
	
	
	
	public String getInteresters() {
		return interesters;
	}

	public void setInteresters(String interesters) {
		this.interesters = interesters;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getSex() {
		return sex;
	}

	public void setSex(int sex) {
		this.sex = sex;
	}

	public String getJieSao() {
		return jieSao;
	}

	public void setJieSao(String jieSao) {
		this.jieSao = jieSao;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public int getFuDaoYuan() {
		return fuDaoYuan;
	}

	public void setFuDaoYuan(int fuDaoYuan) {
		this.fuDaoYuan = fuDaoYuan;
	}
	
	

}
